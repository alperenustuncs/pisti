package com.example.client.model;

public class Student {
    private int id;
    private String name;
    private String surname;
    private String department;
    private int year;
    private double gpa;


    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getSurname() {
        return surname;
    }

    public String getDepartment() {
        return department;
    }

    public int getYear() {
        return year;
    }

    public double getGpa() {
        return gpa;
    }

}
